package main

import (
	"testing"
)

var (
	example1 = `32T3K 765
T55J5 684
KK677 28
KTJJT 220
QQQJA 483`
	// KKKK5 1
	// AAAAA 0`
	example2 = example1
)

func Test_part1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example1",
			input: example1,
			want:  6440,
		},
		// {
		// 	name:  "actual",
		// 	input: input,
		// 	want:  0,
		// },
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

